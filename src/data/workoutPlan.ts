export function getWorkoutById(id?: string, workoutList = workouts) {
  return workoutList.find((workout) => workout.id === id);
}

export function getTodayPlan(plan = weekPlan, date = new Date()) {
  return plan.find((item) => item.dayIndex === date.getDay()) ?? plan[0];
}
